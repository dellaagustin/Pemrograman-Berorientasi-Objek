class luasSegitiga:
    print ('Menghitung Luas Bangun Datar Segitiga')
    alas = input("Alas = ")
    tinggi = input("Tinggi = ")
    luas = (int(alas) * int(tinggi)/2)
    print("Luas = ", luas)

class volumeBalok:
    print ('Menghitung Volume Bangun Ruang Balok')
    panjang = input("Panjang = ")
    lebar = input("Lebar = ")
    tinggi = input("Tinggi = ")
    volume = (int(panjang) * int(lebar) * int(tinggi))
    print("Volume = ", volume)
